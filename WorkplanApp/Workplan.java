    // funkcija, kura pievieno sarakstam ierakstu
    private void addToJList() {
        String s_nr;
        String s_name;
        String s_percent;
        String s_message;
        
        s_nr = jTextField1.getText();       // iegūst projekta numuru
        s_name = jTextField2.getText();     // iegūst projekta nosaukumu
        s_percent = jTextField3.getText();  // iegūst projekta procentus
        
        int n_percent = Integer.parseInt(s_percent);    // pārvēršam projekta procentu uz skaitli
        
        if (n_percent <= 0) {   // ja projekta procenti ir mazāk vai vienāds ar 0
            s_message = "Nevar pievienot projektu ar 0% vai mazāk par 0%.";
            MsgBox(s_message);
        } else if ((nPercentFree - n_percent) < 0) {    // ja brīvie procenti ir mazāk nekā projekta procenti
            s_message = "Nevar pievienot vairāk projektu.";
            MsgBox(s_message);
        } else {
            nPercentFree = nPercentFree - n_percent;    // citādāk atņemam no brīvajiem procentiem projekta procentus
        
            String s_line = s_nr + ", " + 
                    s_name + ", " + 
                    s_percent + "%, " + 
                    calcTime(n_percent, "m") + ", " +
                    calcTime(n_percent, "w") + ", " +
                    calcTime(n_percent, "d");    // izveidojam ieraksta formātu


            lm1.addElement(s_line);         // pievienojam elementu sarakstam
            jList1.setModel(lm1);           // atjaunojam skatu sarakstam
            recalcTime();                   // pārrēķinam laikus
        }
        
    }
    
    private void removeAllEntries() {
        lm1.clear();
        nPercentFree = 100;
        showPercentFree();
    }
    
    private void showPercentFree() {
        jLabel12.setText("Procenti brīvi: " + nPercentFree + "%");
    }
