    private int nPercentFree = 100;     // brīvie procenti
    
    // funkcija, kas parāda laiku
    private void showTime(int percents) {
        jLabel11.setText("5% = " + calcTime(percents, "m")); // izvada laiku
    }
    
    // funkcija, kas pārrēķina laiku
    private void recalcTime() {
        String s_percent = jTextField3.getText();    // iegūst procentus no ievades
        
        int n_percent = Integer.parseInt(s_percent); // tiek parsēts skaitlis
        
        jTextField4.setText(calcTime(n_percent, "m"));  // izvada stundas mēnesī
        jTextField5.setText(calcTime(n_percent, "w"));  // izvada stundas nedēļā
        jTextField6.setText(calcTime(n_percent, "d"));  // izvada stundas dienā
    }
    
    private String calcTime(int percents, String interval) {
        String result = "";
        result = "";
        float time;
        float time1;
        float perc;
        
        time1 = Float.parseFloat(jSpinner1.getValue().toString());  // stundas mēnesī
        
        if (interval.equalsIgnoreCase("m")) {   // ja izvēlētais intervāls ir mēnesis,
            time = time1;                       // tad laiks ir tas pats
        } else if (interval.equalsIgnoreCase("w")) {    // ja izvēlētais intervās ir nedēļa,
            time = time1 / 4;                           // tad dalam ar 4
        } else if (interval.equalsIgnoreCase("d")) {    // ja izvēlētais intervāls ir diena,
            time = time1 / 4 / 7;                       // tad dalam ar 4 un 7
        } else {
            time = time1;
        }
        
        perc = time * percents/100;                    // procenti ir laiks reiz procenti un dalīts uz 100
        
        int hours = (int) perc;                     // stundas
        int mins =  (int) ((perc - hours) * 60);    // minūšu komponenti iegūstam šādi
        //System.out.println(hours);
        //System.out.println(mins);
        if (mins == 0) {                // ja minūšu komponente ir nulle,
            result = (hours + "h");     // tad rezultātā rāda tikai stundas
        } else {                                    // citādāk
            result = (hours + "h" + mins + "m");    // rāda arī minūšu komponenti
        }
        return result;
    }

    private void showPerc5() {              // parāda piecu procentu laiku
        showTime(5);                        // izsauc funkciju, kurai padod 5 procentu parametru
    }
    
